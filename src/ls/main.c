#include <stdio.h>
#include <stdlib.h>
#include <sys/types.h>
#include <sys/stat.h>
#include <unistd.h>
#include <dirent.h>
#include <time.h>
#include <grp.h>
#include <pwd.h>

#include "./lslib.h"

int main(int argc,argv_t argv){
	lsinit();
	int i;
	parse(argc-1,argv+1);
	if(gargc<1)
		list(".");
	else for(i=0;i<gargc;++i){
		printf("%s:\n",gargv[i]);
		list(gargv[i]);
	}
	return 0;
}
